export function getMovie(id) {
  return movies.find((m) => m._id === id);
}

export function getMoviesByGenerId(generId) {
  const items = movies.filter((movie) => generId === movie.genre._id);
  return items;
}

export function saveMovie(movie) {
  let movieInDb = movies.find((m) => m._id === movie._id) || {};
  movieInDb.title = movie.title;
  movieInDb.genre = genresAPI.genres.find((g) => g.name === movie.genre);
  movieInDb.numberInStock = movie.numberInStock;
  movieInDb.dailyRentalRate = movie.dailyRentalRate;

  if (!movieInDb._id) {
    movieInDb._id = Date.now().toString();
    movies.push(movieInDb);
  } else {
    const findIndex = movies.findIndex((mov) => mov._id === movieInDb._id);
    movies[findIndex] = movieInDb;
  }

  return movieInDb;
}

export function deleteMovie(id) {
  let movieInDb = movies.find((m) => m._id === id);
  movies.splice(movies.indexOf(movieInDb), 1);
  return movies;
}

export function searchMovie(title) {
  return movies.filter((m) =>
    m.title.toLowerCase().startsWith(title.toLowerCase())
  );
}
